class Calculadora():
    """
    Clase calculadora con los métodos de cálculo de numeros reales
    :return: numero real resultado del cálculo
    """
    def suma(self, a, b):
        return a + b

    def resta(self, a, b):
        return a - b

    def multiplicacion(self, a, b):
        return a * b

    def division(self, a, b):
        return a / b

